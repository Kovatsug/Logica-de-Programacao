a=int(input("Escolha o primeiro numero: "))
b=int(input("Escolha o segundo numero: "))
c=int(input("Escolha o terceiro numero: "))

if a+b==c:
    print("a soma do primeiro e segundo numero é igual ao terceiro numero")

if a+b>c:
    print("a soma do primeiro e do segundo numero é maior que o terceiro numero")
if a+b<c:
    print("a soma do primeiro e do segundo numero é menor que o terceiro numero")